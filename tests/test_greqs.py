def test_find_module_spec():
    with pytest.raises(ModuleNotFoundError) as e:
        find_module_spec("xxx", True)
    assert e.value.msg == "No module named 'xxx'"

    with pytest.raises(ModuleNotFoundError) as e:
        find_module_spec("xxx.yyy", True)
    assert e.value.msg == "No module named 'xxx'"


